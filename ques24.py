num1=int(input("Enter num1 value"))
num2=int(input("Enter num2 value"))
char=input("Enter any charcter as +,-,*,/")

if(char=="+"):
    print("num1+num2= ",(num1+num2))
elif  (char=="-"):
    print("num1-num2= ",(num1-num2))  
elif(char=="*"):
    print("num1*num2= ",(num1*num2))    
else:
    print("num1/num2= ",(num1/num2))    